# A function that create vowel or consonat alphabet 
def word(ch):
    if ch == "a":
        return "vowel"
    elif ch == "e":
        return "vowel"
    elif ch == "i":
        return "vowel"
    elif ch == "o":
        return "vowel"
    elif ch == "u":
        return "vowel" 
    else:
        return "consonant"
    
